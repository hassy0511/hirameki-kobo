  function lineStory(courseId, lineId) {
    const course = courseStory(courseId);
    return course && course.lines ? course.lines[lineId] || null : null;
  }

  function stageStory(courseId, lineId, stageIndex, stage) {
    const story = lineStory(courseId, lineId);
    const effect = story && story.stageEffects[stageIndex]
      ? story.stageEffects[stageIndex]
      : String(stage && stage.part || '修理パーツ') + 'が動き始めた。';
    return {
      briefing: story ? String(stage && stage.part || 'パーツ') + 'が外れています。' + story.problem : 'この装置へ光が届いていません。',
      mission: story ? story.mission : '装置を直して、工房へ光を戻そう。',
      effect: effect
    };
  }

  function progressStory(courseId, clearedStages, totalStages, completedLines) {
    if (courseId === 'g1') {
      if (completedLines >= 6) return { label: '区画復旧', title: 'ルミナにはじまりの光が戻った！', text: '六つの基礎コアがつながり、はじまり区画の装置が全部動いています。' };
      if (completedLines >= 3) return { label: '復旧後半', title: 'ルミナの光が強くなってきた。', text: 'あと' + (6 - completedLines) + '個のコアで、はじまり区画の基礎回路が全部つながります。' };
      if (completedLines >= 1) return { label: '復旧進行中', title: completedLines + '個のコアが動いている。', text: '直したラインから光が届いています。次のコアも自分の好きな順で作れます。' };
      if (clearedStages > 0) return { label: '修理開始', title: '小さな光が届き始めた。', text: 'パーツを集めて11ステージを直すと、一つのラインコアが完成します。' };
      return { label: '停止中', title: 'ルミナはまだ眠っている。', text: '六つのラインから好きなものを選び、最初の修理エネルギーを送ろう。' };
    }
    if (clearedStages >= totalStages) return { label: '区画復旧', title: '大型装置が全部動いている。', text: 'ひろがり区画の修理が完了しました。' };
    if (clearedStages > 0) return { label: '修理中', title: '大型装置へ光が届いている。', text: '直したパーツをつないで、六つのラインを動かそう。' };
    return { label: '停止中', title: '大型装置が止まっている。', text: '気になるラインの1番から修理を始めよう。' };
  }

